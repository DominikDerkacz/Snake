package snake.enums;

/**
 * Enum {@code GameLevel} reprezentuje poziom trudności rozgrywki.
 * Używany m.in. do skalowania liczby przeszkód oraz czasu między ruchami węża.
 */
public enum GameLevel {
    /** Łatwy poziom trudności. */
    EASY,

    /** Średni poziom trudności. */
    MEDIUM,

    /** Trudny poziom trudności. */
    HARD
}
